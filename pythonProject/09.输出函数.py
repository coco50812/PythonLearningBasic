"""
输出函数
    1. 普通输出
    2. 格式化输出
    3. format输出
    4. f 表达式
"""
# 普通输出 => print 直接输出
print("你好!")

# 格式化输出 => 填坑挖坑  %s => 字符串     %d => 数字
print("我叫做%s,我今年%d岁"%("文种玉",18))
# print("我叫做%d"%("文种玉")) # %d 不能填充字符串

# format输出 => {} 留坑,format后面来进行填
print("大家好,我叫做{},今年{}岁,喜欢打{}".format("文种玉",18,"篮球"))

# f 表达式 => 可以写变量在字符串当中  用大括号
name = "文种玉"
age = 18
gender = "男"
print(f"大家好,我叫做{name},今年{age}岁,性别{gender}")