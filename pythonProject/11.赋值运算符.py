"""
赋值运算符
    =       赋值运算符,将=号左边的内容赋值给等号的右边
    +=      a += b      =>      a = a + b
    -=      a -= b      =>      a = a - b
    *=      a *= b      =>      a = a * b
    /=      a /= b      =>      a = a /= b
    //=     a //= b     =>      a = a // b
    %=      a %=  b     =>      a = a % b
    **=     a **= b     =>      a = a ** b
"""
a = 10
b = 3
a += b
print(a)

a -= b
print(a)

a *= b
print(a)

a /= b
print(a)

a //= b
print(a)

a %= b
print(a)

a **= b
print(a)